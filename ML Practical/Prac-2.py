# OR
a = True
b = False
result_or = a or b
print("OR:", result_or)

# AND
c = True
d = False
result_and = c and d
print("AND:", result_and)

# Checking for Equality
e = 5
f = 10
result_equal = e == f
print("Equal:", result_equal)

# NOT
g = True
result_not = not g
print("NOT:", result_not)

# XOR
h = True
i = False
result_xor = h != i
print("XOR:", result_xor)
